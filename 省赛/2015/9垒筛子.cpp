#include<iostream>
using namespace std;
#define MOD 1000000007
#define max_n 100000000
int ans=0;
int op[7];
bool con[7][7]={0};
int f(int upp,int cnt){
	int ans=0;
	int downn=op[upp];
	if(cnt==0)return 1;
	for(int up=1;up<=6;up++){
		if(con[downn][up]){
			continue;
		}
		ans+=(4*f(up,cnt-1))%MOD;
	}
	return ans;
}
int main(){
	op[1]=4,op[4]=1;
	op[2]=5,op[5]=2;
	op[3]=6,op[6]=3;
	
	int n,m,x,y;
	cin>>n>>m;
	for(int i=0;i<m;i++){
		cin>>x>>y;
		con[x][y]=true;
		con[y][x]=true;
	}
	for(int up=1;up<=6;up++){
		ans+=(4*f(up,n-1))%MOD;
	}

	cout<<ans;
	return 0;
}

